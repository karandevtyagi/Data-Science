import requests
import json
head='"ID","JOKE"'
head+='\n'
filename='chuck_norris.csv'
with open(filename,'w') as f:
    f.write(head)
    for i in range(1,500):
        url="http://api.icndb.com/jokes/{}".format(i)
        r=requests.get(url)
        r=r.content.decode('utf-8')
        data=json.loads(r)
        if(data['value']=='No quote with id=%d.'%(i)):
            continue
        if(data['value']['joke'].find(',')>-1):
            row=str(data['value']['id'])+','+'"'+data['value']['joke']+'"'+'\n'
        else:
            row=str(data['value']['id'])+','+data['value']['joke']+'\n'
        f.write(row)




